import { matchWon } from './connectFour';

test('vertical red', () => {
  expect(matchWon([
    ['', '', '', '', '', '', ''],
    ['', '', '', '', '', '', ''],
    ['R', '', '', '', '', '', ''],
    ['R', '', '', '', '', '', ''],
    ['R', '', '', '', '', '', ''],
    ['R', 'B', 'B', 'R', '', '', '']
], 2, 0)).toBe('R');
});

test('missing one to vertical red', () => {
    expect(matchWon([
      ['', '', '', '', '', '', ''],
      ['', '', '', '', '', '', ''],
      ['', '', '', '', '', '', ''],
      ['R', '', '', '', '', '', ''],
      ['R', '', '', '', '', '', ''],
      ['R', 'B', 'B', 'R', '', '', '']
  ], 2, 0)).toBe(undefined);
  });